public class Prueba5 {

   
    public static void main(String[] args) {
        double rad = 6.5;
        double pi = 3.14159;
        double a = 2.0;
        double perimetro;
        double area;
        
        perimetro = a * rad * pi;
        area = rad * rad * pi;
        
        System.out.println("El resultado del Perimetro es = " + perimetro);
        System.out.println("El resultado del area es = " + area);
    }
    
}
