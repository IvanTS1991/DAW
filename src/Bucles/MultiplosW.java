package Bucles;

public class MultiplosW {

	public static void main(String[] args) {
	
		//Utilizando  while
		System.out.println("Estos son los multiplos del 5 empezando desde 0 hasta 100 usando un bucle while: ");
			
			int num = 0;
			while (num < 101) {
				System.out.println(num);
				num+=5;
			}
	}

}
