package Bucles;

public class ContadorDecrementoFor {

	public static void main(String[] args) {
		
		//Muestra los números del 320 al 160, contando de 20 en 20 utilizando un bucle for
		
			System.out.println("Muestra los numeros comprendidos entre 320 y 160 de 20 en 20 usando un bucle for: ");
				
			
			
			for (int num=320; num >= 160; num -= 20) {
				System.out.println(num);
			}
	}

}
