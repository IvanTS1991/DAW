package Bucles;

public class MultiplosFor {

	public static void main(String[] args) {
		
		//Utilizando for
		System.out.println("Estos son los multiplos de 5 del 0 a 100 usando un bucle for: ");
		
		for(int num = 0; num <= 100; num += 5) {
		      System.out.println(num);
		    }
	}

}
