package Bucles;

public class MultiplosDW {

	public static void main(String[] args) {
		
		//Utilizando  do while
		System.out.println("Estos son los multiplos de 5 del 0 a 100 con bucle do while: ");
			
		 int num = 0;
		    
		    do {
		      System.out.println(num);
		      num+=5;
		    } while(num <= 100);
	}

}
